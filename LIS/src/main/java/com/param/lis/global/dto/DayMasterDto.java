package com.param.lis.global.dto;

public class DayMasterDto {

	private Integer dayMasterId;
	private Integer day;
	/**
	 * @return the dayMasterId
	 */
	public Integer getDayMasterId() {
		return dayMasterId;
	}
	/**
	 * @param dayMasterId the dayMasterId to set
	 */
	public void setDayMasterId(Integer dayMasterId) {
		this.dayMasterId = dayMasterId;
	}
	/**
	 * @return the day
	 */
	public Integer getDay() {
		return day;
	}
	/**
	 * @param day the day to set
	 */
	public void setDay(Integer day) {
		this.day = day;
	}
	
}
